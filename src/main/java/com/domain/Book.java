package com.domain;

import java.util.Date;

public class Book {

	private String book_name;
	private int book_id;
	private double book_price;
	private Date book_publish_date;
	private String book_press;
	private String book_author;
	private int book_lv;
	private String book_type;
	private String book_isbn;
	public String getBook_name() {
		return book_name;
	}
	public void setBook_name(String book_name) {
		this.book_name = book_name;
	}
	public int getBook_id() {
		return book_id;
	}
	public void setBook_id(int book_id) {
		this.book_id = book_id;
	}
	public double getBook_price() {
		return book_price;
	}
	public void setBook_price(double book_price) {
		this.book_price = book_price;
	}
	public Date getBook_publish_date() {
		return book_publish_date;
	}
	public void setBook_publish_date(Date book_publish_date) {
		this.book_publish_date = book_publish_date;
	}
	public String getBook_press() {
		return book_press;
	}
	public void setBook_press(String book_press) {
		this.book_press = book_press;
	}
	public String getBook_author() {
		return book_author;
	}
	public void setBook_author(String book_author) {
		this.book_author = book_author;
	}
	public int getBook_lv() {
		return book_lv;
	}
	public void setBook_lv(int book_lv) {
		this.book_lv = book_lv;
	}
	public String getBook_type() {
		return book_type;
	}
	public void setBook_type(String book_type) {
		this.book_type = book_type;
	}
	public String getBook_isbn() {
		return book_isbn;
	}
	public void setBook_isbn(String book_isbn) {
		this.book_isbn = book_isbn;
	}
	
}
